    <h1>Apa Itu Framework?</h1>
    <p>Ketika mengacu pada software komputer, pengertian framework adalah fondasi dengan tingkat kerumitan</p>
    <p>yang ditentukan dan dapat diperluas oleh pemrogram dengan menggunakan kode mereka sendiri termasuk</p>
    <p>kompiler, juru bahasa, atau API. Secara umum, framework menyediakan lingkungan yang memfasilitasi</p>
    <p>jenis pemrograman tertentu untuk proyek pengembangan software.</p>
    <p></p>
    <p></p>
    <p style="left: 10%;position: relative">Sebuah framework dalam pemrograman seringkali merupakan struktur</p>
    <p style="left: 10%;position: relative">berlapis yang menunjukkan program apa yang dapat atau harus dibangun</p>
    <p style="left: 10%;position: relative">dan bagaimana framework akan saling terkait.</p>
    <p></p>
    <p></p>
    <p>Beberapa framework sistem komputer juga termasuk program aktual,</p>
    <p>menentukan interface pemrograman, atau menawarkan alat pemrograman</p>
    <p>untuk menggunakan kerangka kerja. Suatu framework mungkin digunakan</p>
    <p>untuk sekumpulan fungsi dalam suatu sistem dan bagaimana semuanya saling terkait.</p>
